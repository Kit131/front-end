                    <h2>
                        Understanding the Process
                    </h2>
                    <p>In order to collect on an injury claim in Virginia, a personal injury victim (plaintiff) must prove the person who caused the injury (plaintiff) was negligent. The following must be proved:</p>
                    <ul>
                        <li>The existence of a duty owed to you by the person who caused your injury</li>
                        <li>The other person failed to carry out the duty that they owed you</li>
                        <li>You suffered damages</li>
                        <li>The other person’s failure caused you to have the injury</li>
                    </ul>
                    <p>If you in any way contributed to your injury, you may not be able to recover damages in Virginia under the principle of “contributory negligence” – which means that your own carelessness, when combined with the carelessness of the other person, caused you to be injured.</p>
                    <p>There also exists in Virginia the principle of “joint and several liability.” If more than one person is negligent toward you, each person who has been found negligent is responsible for the total damages under this principle.</p>
